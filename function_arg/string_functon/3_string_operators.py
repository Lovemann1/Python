# operators in python 
a = "hello"     
b = "pyhon"
print(a + b)    # + in string

print (a *3)    # * in string

if "h" in a:    # in in string
    print("yes")
else:
    print("no")

if "h" not in a:    # not_in in string
    print("yes")
else:
    print("no")

# r is row string  suppress the escape chars
print(r"hellow\n world")

